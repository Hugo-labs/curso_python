# Operadores de comparação

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 1 <= 1
igual = 'a' == 'a'
diferente = 'a' != 'b'

print(f'2 > 1 = {maior}')
print(f'2 >= 2 = {maior_ou_igual}')
print(f'1 < 2 = {menor}')
print(f'1 <= 1 = {menor_ou_igual}')
print(f'"a" = "a" = {igual}')
print(f'"a" != "b" = {diferente}')